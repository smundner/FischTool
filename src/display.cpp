void sendCommand(byte c) {
  Wire.beginTransmission(DISPLAY_ADDRESS);
  Wire.write(0x00);
  Wire.write(c);
  Wire.endTransmission();
}

void Display::begin() {
  sendCommand(DISPLAY_DISPLAYOFF);
  sendCommand(DISPLAY_SETDISPLAYCLOCKDIV);
  sendCommand(0x80);

  sendCommand(DISPLAY_SETMULTIPLEX);
  sendCommand(63);

  sendCommand(DISPLAY_SETDISPLAYOFFSET);
  sendCommand(0x00);
  sendCommand(DISPLAY_SETSTARTLINE | 0x0);
  sendCommand(DISPLAY_CHARGEPUMP);
  sendCommand(0x14);

  sendCommand(DISPLAY_MEMORYMODE);
  sendCommand(0x00);
  sendCommand(DISPLAY_SEGREMAP | 0x1);
  sendCommand(DISPLAY_COMSCANDEC);

  sendCommand(DISPLAY_SETCOMPINS);
  sendCommand(0x12);
  sendCommand(DISPLAY_SETCONTRAST);
  sendCommand(0xCF);

  sendCommand(DISPLAY_SETPRECHARGE);
  sendCommand(0xF1);
  sendCommand(DISPLAY_SETVCOMDETECT);
  sendCommand(0x40);
  sendCommand(DISPLAY_DISPLAYALLON_RESUME);
  sendCommand(DISPLAY_NORMALDISPLAY);

  sendCommand(DISPLAY_DEACTIVATE_SCROLL);

  sendCommand(DISPLAY_DISPLAYON);
}

