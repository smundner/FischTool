void Display::clear(){
  for(int16_t i = 0;i<(128 * 64) / 8;i++){
    display_buffer[i]=0x00;
    }
  }

void Display::display() {

  sendCommand(DISPLAY_COLUMNADDR);
  sendCommand(0);
  sendCommand(127);
  sendCommand(DISPLAY_PAGEADDR);
  sendCommand(0);
  sendCommand(7);

  for (uint16_t i = 0; i < (128 * 64) / 8; i++) {
    Wire.beginTransmission(DISPLAY_ADDRESS);
    Wire.write(0x40);
    for (uint16_t j = 0; j < 16; j++) {
      Wire.write(display_buffer[i]);
      //Serial.print(i);Serial.print(": ");Serial.println(display_buffer[i]);
      i++;
    }
    i--;
    Wire.endTransmission();
  }
}

void Display::writeChars(uint8_t xoff,uint8_t yoff,char *data){
  while(*data!='\0'){
  for(int8_t i=0; i<5; i++ ) { // Char bitmap = 5 columns
    uint8_t line = pgm_read_byte(&font[*data * 5 + i]);
    for(int8_t j=0;j<7;j++,line>>=1){
      
      if(line & 1){
        drawPixel(xoff+i,yoff+j,1);
      }else{
        drawPixel(xoff+i,yoff+j,0);
        }
      }
    }
    xoff +=8;
    
    data++;
  }  
  }
